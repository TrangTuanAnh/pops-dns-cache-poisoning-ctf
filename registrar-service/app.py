"""Registrar service.

Player POST /api/v1/register voi {"domain": "<name>.example", "ns_ip": "<ip>"}
de "dang ky" mot domain trong lab. Service:
  1. Validate input (domain phai .example, ns_ip phai trong subnet challenge).
  2. Sinh zone file glue: dynamic/<name>.example.zone voi NS record tro
     ve ns_ip cua attacker.
  3. Append zone vao /shared/zones-dynamic.conf (file include cua nsd.conf).
  4. Touch /shared/reload-trigger.txt; nsd-auth container co watcher
     reload khi trigger thay doi.

Endpoint:
  GET  /health
  GET  /api/v1/registrations
  POST /api/v1/register
  POST /api/v1/zone-trigger-reload
"""
from __future__ import annotations

import ipaddress
import json
import logging
import os
import re
import sys
import time
from http.server import BaseHTTPRequestHandler, ThreadingHTTPServer
from pathlib import Path
from urllib.parse import urlparse


log = logging.getLogger("registrar")


STATE_PATH = Path(os.environ.get("REGISTRAR_STATE_PATH", "/data/registrations.json"))
SHARED_ZONES_DIR = Path(os.environ.get("REGISTRAR_SHARED_ZONES", "/shared/zones"))
SHARED_INCLUDE = Path(os.environ.get(
    "REGISTRAR_SHARED_INCLUDE", "/shared/zones-dynamic.conf"
))
TRIGGER_FILE = Path(os.environ.get("REGISTRAR_TRIGGER", "/shared/reload-trigger.txt"))
ALLOWED_SUBNETS = os.environ.get(
    "REGISTRAR_ALLOWED_SUBNETS",
    "10.0.0.0/24,10.0.2.0/24,10.0.3.0/24",
).split(",")


_DOMAIN_RE = re.compile(
    r"^[a-z0-9]([a-z0-9-]*[a-z0-9])?(\.[a-z0-9]([a-z0-9-]*[a-z0-9])?)*$"
)


def _validate_domain(domain: str) -> str | None:
    if not domain or not domain.endswith(".example"):
        return "domain phai ket thuc bang .example"
    if not _DOMAIN_RE.match(domain):
        return "domain khong dung syntax"
    if domain.startswith("meridian"):
        return "khong duoc dang ky domain bat dau bang 'meridian' (reserved)"
    if domain.count(".") < 1:
        return "domain phai co it nhat 1 label truoc .example"
    return None


def _validate_ip(ip: str) -> str | None:
    try:
        addr = ipaddress.ip_address(ip)
    except ValueError:
        return "ns_ip khong phai IP hop le"
    for subnet in ALLOWED_SUBNETS:
        try:
            if addr in ipaddress.ip_network(subnet.strip(), strict=False):
                return None
        except ValueError:
            continue
    return f"ns_ip khong nam trong subnet cho phep ({','.join(ALLOWED_SUBNETS)})"


def _read_state() -> dict:
    if not STATE_PATH.exists():
        return {"registrations": []}
    try:
        with STATE_PATH.open("r", encoding="utf-8") as h:
            return json.load(h)
    except Exception:
        return {"registrations": []}


def _write_state(state: dict) -> None:
    STATE_PATH.parent.mkdir(parents=True, exist_ok=True)
    with STATE_PATH.open("w", encoding="utf-8") as h:
        json.dump(state, h, indent=2, sort_keys=True)


def _generate_zone(domain: str, ns_ip: str) -> str:
    ns_label = "ns1." + domain
    return (
        f"$ORIGIN {domain}.\n"
        f"$TTL 60\n"
        f"@ IN SOA {ns_label}. admin.{domain}. (\n"
        f"    {int(time.time())} ; serial\n"
        f"    60 30 120 60\n"
        f")\n"
        f"@ IN NS {ns_label}.\n"
        f"ns1 IN A {ns_ip}\n"
    )


def _generate_include_block(domain: str) -> str:
    safe = domain.replace("/", "_")
    return (
        f"zone:\n"
        f"  name: \"{domain}\"\n"
        f"  zonefile: \"dynamic/{safe}.zone\"\n"
    )


def _persist_zone(domain: str, ns_ip: str) -> None:
    SHARED_ZONES_DIR.mkdir(parents=True, exist_ok=True)
    safe = domain.replace("/", "_")
    zone_path = SHARED_ZONES_DIR / f"{safe}.zone"
    zone_path.write_text(_generate_zone(domain, ns_ip), encoding="ascii")
    log.info("wrote zone file %s", zone_path)
    SHARED_INCLUDE.parent.mkdir(parents=True, exist_ok=True)
    existing = (
        SHARED_INCLUDE.read_text(encoding="ascii")
        if SHARED_INCLUDE.exists()
        else ""
    )
    block = _generate_include_block(domain)
    if block.strip() not in existing:
        with SHARED_INCLUDE.open("a", encoding="ascii") as h:
            h.write("\n# auto-generated by registrar\n" + block)
    TRIGGER_FILE.parent.mkdir(parents=True, exist_ok=True)
    TRIGGER_FILE.write_text(f"reload {time.time()}\n", encoding="ascii")
    log.info("touched reload trigger %s", TRIGGER_FILE)


def _json(handler: BaseHTTPRequestHandler, status: int, body: dict) -> None:
    payload = json.dumps(body, indent=2, sort_keys=True).encode("utf-8")
    handler.send_response(status)
    handler.send_header("Content-Type", "application/json")
    handler.send_header("Content-Length", str(len(payload)))
    handler.end_headers()
    handler.wfile.write(payload)


class RegistrarHandler(BaseHTTPRequestHandler):
    server_version = "pops-ctf-registrar/0.2"

    def do_GET(self) -> None:
        path = urlparse(self.path).path
        if path == "/health":
            _json(self, 200, {"ok": True, "service": "registrar"})
            return
        if path == "/api/v1/registrations":
            _json(self, 200, _read_state())
            return
        _json(self, 404, {"error": "not_found"})

    def do_POST(self) -> None:
        path = urlparse(self.path).path
        length = int(self.headers.get("Content-Length", "0"))
        try:
            payload = json.loads(self.rfile.read(length) or b"{}")
        except json.JSONDecodeError:
            _json(self, 400, {"error": "invalid_json"})
            return

        if path == "/api/v1/zone-trigger-reload":
            TRIGGER_FILE.parent.mkdir(parents=True, exist_ok=True)
            TRIGGER_FILE.write_text(f"manual {time.time()}\n", encoding="ascii")
            _json(self, 202, {"status": "trigger_emitted"})
            return

        if path != "/api/v1/register":
            _json(self, 404, {"error": "not_found"})
            return

        domain = str(payload.get("domain", "")).strip().lower().rstrip(".")
        ns_ip = str(payload.get("ns_ip", "")).strip()

        err = _validate_domain(domain) or _validate_ip(ns_ip)
        if err:
            _json(self, 400, {"error": err})
            return

        state = _read_state()
        existing = [r for r in state["registrations"] if r["domain"] == domain]
        if existing:
            _json(self, 409, {"error": f"{domain} da duoc dang ky"})
            return

        try:
            _persist_zone(domain, ns_ip)
        except Exception as exc:
            log.exception("persist zone failed")
            _json(self, 500, {"error": "persist_failed", "detail": str(exc)})
            return

        state["registrations"].append({
            "domain": domain,
            "ns_ip": ns_ip,
            "registered_at": time.time(),
        })
        _write_state(state)
        _json(
            self,
            202,
            {
                "status": "registered",
                "domain": domain,
                "ns_ip": ns_ip,
                "note": (
                    "Zone file da ghi vao shared volume; NSD se reload trong "
                    "vai giay. Sau do tu host authoritative cua ban tai ns_ip "
                    "tren port 53 va trigger query qua resolver lab."
                ),
            },
        )

    def log_message(self, fmt, *args):
        log.info("api " + fmt, *args)


def main() -> int:
    logging.basicConfig(
        format="%(asctime)s %(levelname)s %(name)s %(message)s",
        level=logging.INFO,
        stream=sys.stdout,
    )
    port = int(os.environ.get("PORT", "8080"))
    server = ThreadingHTTPServer(("0.0.0.0", port), RegistrarHandler)
    log.info("registrar listening on %d", port)
    server.serve_forever()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
